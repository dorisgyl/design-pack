// sync-pack-seeds.mjs — regenerate pack.toml's [[components.seed]] list from disk.
//
// Scans components/seed/**/*.md, reads each page's slug, and rewrites the block
// between the generated-region markers in pack.toml. Seed files are the source of
// truth; the manifest's seed list is derived. Run after adding/removing seeds.
import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs';
import { join, dirname, resolve, relative } from 'node:path';
import { fileURLToPath } from 'node:url';
import { readPage } from './frontmatter.mjs';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const SEED_DIR = join(ROOT, 'components', 'seed');
const TOML = join(ROOT, 'pack.toml');
const BEGIN = '# >>> seeds (generated by scripts/sync-pack-seeds.mjs — do not edit by hand) >>>';
const END = '# <<< seeds (generated) <<<';

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (name.endsWith('.md')) out.push(p);
  }
  return out;
}

const files = walk(SEED_DIR).sort();
const entries = [];
for (const f of files) {
  const { data } = readPage(f);
  if (!data.slug) {
    console.error(`FAIL: ${relative(ROOT, f)} has no slug in frontmatter`);
    process.exit(1);
  }
  const from = relative(ROOT, f).split('\\').join('/');
  entries.push({ slug: data.slug, from });
}
entries.sort((a, b) => a.slug.localeCompare(b.slug));

const block = entries
  .map((e) => `[[components.seed]]\nslug = "${e.slug}"\nfrom = "${e.from}"`)
  .join('\n\n');

const toml = readFileSync(TOML, 'utf8');
const re = new RegExp(
  `${BEGIN.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}[\\s\\S]*?${END.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}`
);
if (!re.test(toml)) {
  console.error('FAIL: generated-region markers not found in pack.toml');
  process.exit(1);
}
const next = toml.replace(re, `${BEGIN}\n${block}\n${END}`);
writeFileSync(TOML, next, 'utf8');
console.log(`synced ${entries.length} seed entries into pack.toml`);
